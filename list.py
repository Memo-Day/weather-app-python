# list of 3 countries
countries = ["Japan", "Russia", "Turkish"]

# Print out the list
print(countries)

#Remove the last country
countries.pop()
#Print out the list
print(countries)

#Add another element at the beginning of the list
countries.insert(0, "Greece")
#Print out the list
print(countries)
#Print out the length of the list
print(len(countries)) # => 3
#Sort the list 
countries.sort()
#Print out the list
print(countries)